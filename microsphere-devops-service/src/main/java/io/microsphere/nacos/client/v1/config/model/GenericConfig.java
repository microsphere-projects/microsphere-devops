package io.microsphere.nacos.client.v1.config.model;

import io.microsphere.nacos.client.common.model.Model;

/**
 * The Generic {@link Model model} {@link Class} for Nacos Configuration
 *
 * @author <a href="mailto:mercyblitz@gmail.com">Mercy<a/>
 * @see BaseConfig
 * @since 1.0.0
 */
public class GenericConfig extends BaseConfig {

    private static final long serialVersionUID = -5758504468972397705L;

    private String operatorIp;

    private Long createdTime;

    private Long lastModifiedTime;

    public String getOperatorIp() {
        return operatorIp;
    }

    public void setOperatorIp(String operatorIp) {
        this.operatorIp = operatorIp;
    }

    public Long getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(Long createdTime) {
        this.createdTime = createdTime;
    }

    public Long getLastModifiedTime() {
        return lastModifiedTime;
    }

    public void setLastModifiedTime(Long lastModifiedTime) {
        this.lastModifiedTime = lastModifiedTime;
    }
}
